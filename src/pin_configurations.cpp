    pinMode(INPUT_PUMP_5_RUNNING_PIN, INPUT_PULLUP);

    pinMode(OUTPUT_PUMP_6_ENABLE_PIN, OUTPUT);
    pinMode(INPUT_PUMP_6_RUNNING_PIN, INPUT_PULLUP);

    
