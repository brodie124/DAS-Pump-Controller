    digitalWrite(OUTPUT_PUMP_2_ENABLE_PIN, PUMP_ENABLE_PIN_OFF);
    digitalWrite(OUTPUT_PUMP_3_ENABLE_PIN, PUMP_ENABLE_PIN_OFF);
    digitalWrite(OUTPUT_PUMP_4_ENABLE_PIN, PUMP_ENABLE_PIN_OFF);
    digitalWrite(OUTPUT_PUMP_5_ENABLE_PIN, PUMP_ENABLE_PIN_OFF);
